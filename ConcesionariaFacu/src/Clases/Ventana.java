    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        // TODO add your handling code here:
        if(rbtCoche.isSelected()){
            int anio = Integer.parseInt(txtAnio.getText());
            int puertas = Integer.parseInt(txtPuertas.getText());
            coche = new Coche(txtMarca.getText(),txtModelo.getText(),anio,puertas);
            foto = new ImageIcon(getClass().getResource("coche.jpg"));
            lblImagen.setIcon(foto);
        }
        else if (rbtMoto.isSelected()){
            int anio = Integer.parseInt(txtAnio.getText());
            boolean casco=true;
            if(rbtSi.isSelected()){
                casco = true;
            }
            else if (rbtNo.isSelected()){
                casco = false;
            }
            foto = new ImageIcon(getClass().getResource("moto.jpg"));
            moto = new Moto(txtMarca.getText(),txtModelo.getText(),anio,casco);
            lblImagen.setIcon(foto);
        }
    }//GEN-LAST:event_btnCargarActionPerformed

