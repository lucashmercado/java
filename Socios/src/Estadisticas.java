    public Estadisticas() {
      initComponents();
      
      //Declaro los contadores y los inicializo en cero
      int cTotal = 0;
      int cFutsal = 0;
      int cNatacion = 0;
      int cGimnasio = 0;
      int cMenores = 0;
      int cAdultos = 0;
      int cMayores = 0;
      int cMasculino = 0;
      int cFemenino = 0;
      
      //Recorro el arrayList y actualizo contadores
      cTotal = AltasSocios.nomina.size();
      for(int i=0; i<cTotal; i++){
          if(AltasSocios.nomina.get(i).isFutsal()){
              cFutsal++;
          }
          if(AltasSocios.nomina.get(i).isNatacion()){
              cNatacion++;
          }
          if(AltasSocios.nomina.get(i).isGimnasio()){
              cGimnasio++;
          }
          if(AltasSocios.nomina.get(i).getEdad()==17){
              cMenores++;
          }
          if(AltasSocios.nomina.get(i).getEdad()==29 || AltasSocios.nomina.get(i).getEdad()==59){
              cAdultos++;
          }
          if(AltasSocios.nomina.get(i).getEdad()==99){
              cMayores++;
          }
          if(AltasSocios.nomina.get(i).getSexo().equals("Femenino")){
              cFemenino++;
          }
          if(AltasSocios.nomina.get(i).getSexo().equals("Masculino")){
              cMasculino++;
          }
      }
      //Vuelco el contenido de los contadores en los cuadros de texto
      txtAnotados.setText(String.valueOf(cTotal));
      txtFutsal.setText(String.valueOf(cFutsal));
      txtNatacion.setText(String.valueOf(cNatacion));
      txtGimnasio.setText(String.valueOf(cGimnasio));
      txtMenores.setText(String.valueOf(cMenores));
      txtAdultos.setText(String.valueOf(cAdultos));
      txtMayores.setText(String.valueOf(cMayores));
      txtMasculino.setText(String.valueOf(cMasculino));
      txtFemenino.setText(String.valueOf(cFemenino));
    }
      
     

