    private void btCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCargarActionPerformed
        // TODO add your handling code here:
        Coche c1=new Coche();
        Moto m1=new Moto();
        if(rbAuto.isSelected()){
          c1.Marca(txtMarca.getText());
          c1.Modelo(txtModelo.getText());
          c1.anio(Double.valueOf(txtAnio.getText()));
          c1.numerosDePuertas(Double.valueOf(txtPuertas.getText()));
          ImageIcon coche2 = new ImageIcon("coche.jpg");
          lbFoto.setIcon(coche2);
        }else {
            m1.Marca(txtMarca.getText());
          m1.Modelo(txtModelo.getText());
          m1.anio(Double.valueOf(txtAnio.getText()));
         
        }
    }//GEN-LAST:event_btCargarActionPerformed

